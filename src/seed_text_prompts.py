async def seed_text():
    await init_db()
    async with async_session_maker() as session:
        logger.info("Adding high-quality text prompts...")
        count = 0
        for item in NEW_TEXT_PROMPTS:
            prompt_id = str(uuid.uuid4())
            prompt = Prompt(
                id=prompt_id,
                title=item["title"],
                prompt_text=item["prompt_text"],
                preview_url=None,
                media_type="text",
                category=item["category"],
                likes_count=100 + (count * 15)
            )
            session.add(prompt)
            count += 1
        
        await session.commit()
        logger.info(f"Successfully added {count} text prompts.")

if __name__ == "__main__":
    asyncio.run(seed_text())
